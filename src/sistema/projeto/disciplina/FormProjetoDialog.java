    private void carregarEquipes() {
        cbEquipe.removeAllItems();
        equipeIds.clear();
        cbEquipe.addItem("— Sem equipe —"); // índice 0 = sem equipe
        equipeIds.add(null);

        EquipeDAO dao = new EquipeDAO();
        for (Equipe e : dao.findAll()) {
            cbEquipe.addItem(e.getNome());
            equipeIds.add(e.getId());
        }
    }



    // Permite abrir o diálogo em modo edição
    public void setProjeto(Projeto p) {
        this.projeto = p;

        // título/botão
        jLabel7.setText("Editar Projeto");
        btnSalvar.setText("Salvar");

        // Preenche campos
        txtNome.setText(nullSafe(p.getNome()));
        txtDescricao.setText(nullSafe(p.getDescricao()));
        cbStatus.setSelectedItem(nullSafe(p.getStatus()));

        txtDataInicio.setText(p.getDataInicio() != null ? BR.format(p.getDataInicio()) : "");
        txtDataFim.setText(p.getDataFim() != null ? BR.format(p.getDataFim()) : "");

        // Seleciona gestor no combo
        if (p.getEquipeId() == null) {
        cbEquipe.setSelectedIndex(0); // — Sem gestor —
        } else {
        // procura o índice cujo id bate
        int found = 0;
        for (int i = 0; i < equipeIds.size(); i++) {
            Long id = equipeIds.get(i);
            if (id != null && id.equals(p.getEquipeId())) { found = i; break; }
        }
        cbEquipe.setSelectedIndex(found);
               
        }
    }
       

