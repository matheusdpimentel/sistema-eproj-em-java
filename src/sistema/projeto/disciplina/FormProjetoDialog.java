    private String nullSafe(String s) { return s == null ? "" : s; }

    private LocalDate parseDate(String s) {
        s = s == null ? "" : s.trim();
        if (s.isEmpty()) return null;
        try {
            return LocalDate.parse(s, BR);
        } catch (DateTimeParseException e) {
            throw new IllegalArgumentException("Data inválida (use dd/MM/yyyy): " + s);
        }
    }

   private Long getEquipeIdSelecionado() {
    int idx = cbEquipe.getSelectedIndex();
    if (idx < 0) return null;
    return equipeIds.get(idx);
}

    // Monta/atualiza o projeto a partir dos campos
    private Projeto coletarProjetoDoForm() {
        String nome = txtNome.getText().trim();
        String status = (String) cbStatus.getSelectedItem();
        String descricao = txtDescricao.getText().trim();
        LocalDate dtInicio = parseDate(txtDataInicio.getText());
        LocalDate dtFim = parseDate(txtDataFim.getText());
        Long equipeId = getEquipeIdSelecionado();

        
        if (nome.isEmpty()) throw new IllegalArgumentException("Informe o nome do projeto.");
        if (status == null || status.isEmpty()) throw new IllegalArgumentException("Selecione um status.");

        if (dtInicio != null && dtFim != null && dtFim.isBefore(dtInicio)) {
            throw new IllegalArgumentException("Data fim não pode ser anterior à data início.");
        }

        Projeto p = (projeto != null) ? projeto : new Projeto();
        p.setNome(nome);
        p.setStatus(status);
        p.setDescricao(descricao);
        p.setDataInicio(dtInicio);
        p.setDataFim(dtFim);
        p.setEquipeId(equipeId);

        return p;
    }


